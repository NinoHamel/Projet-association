<h1> Yume Nikki </h1>
<h2> Gratuit sur steam </h2> 
<h4> Yume Nikki est un jeu indépendant assez cryptique. Difficile à classer, il appartient à un genre entre l'exploration et l'horreur psychologique. Vous incarnez Madotsuki,
une jeune fille qui ne peut sortir de sa chambre. Cependant vous pouvez dormir dans son lit, qui vous emmènera dans le monde des rêves, où se déroule tout le jeu. Vous vous
verrez donc explorer ce vaste monde à la recherche de connexions vers d'autres mondes, en train d'analyser ce que peuvent bien signifier les différents éléments qui vont joncher
votre parcours. Ce qui rend ce jeu original, c'est qu'il ne s'agit pas d'un jeu palpitant où vous serez immédiatement satisfais, l'admiration pour ce jeu se fait au fil du temps,
lorsque vous repenserez à ce que vous avez vu, l'ambiance que vous avez vécu, et toutes les théories farfelues que vous pourrez attribuer à de simples choses.
On ne garantit pas que le jeu vous plaira, mais il vaut toujours le coup de l'essayer, un soir sans lumière avec votre casque sur les oreilles.</h4>
