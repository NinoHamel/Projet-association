<h1> Risk of rain 2 </h1>
<h2> 24.99€ sur steam </h2> 
<h4> Risk of rain 2 est un excellent jeu que nous vous recommandons : c'est un roguelike, les graphismes sont uniques, le gameplay extrêmement bien étudié ( prise en main rapide 
mais qui a tout-de même de la complexité sous le pied ). Le jeu reprend donc les mécaniques classiques des rogue like : on doit traverser des zones remplies
d'ennemis pour activer un téléporteur qui permet de combattre un boss de zone et de passer à la prochaine. Tout cela en récupérant des objets le long de ces zones. Les possibilités
sont nombreuses : débloquer de nouveaux personnages, débloquer de nouvelles fonctionnalités sur ceux que vous possédez déjà, speedrun pour finir le jeu de plus en plus vite, ...
Vous en aurez donc pour votre argent car vous allez très vite devenir accro à ce jeu et passer beaucoup d'heures dessus.
Vous pouvez jouer seul où à plusieurs ( 1 à 4 joueurs ) en ligne.</h4>
